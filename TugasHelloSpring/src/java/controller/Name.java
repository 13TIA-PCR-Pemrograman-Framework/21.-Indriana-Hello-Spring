public class Name {
    private String name;
    private int nim;
    private String address;
    
    public String getname() {
        return name;
    }

    public void setname(String name) {
        this.name = name;
    }
    
    public int getnim() {
        return nim;
    }

    public void setnim(int nim) {
        this.nim = nim;
    }

    public String getaddress() {
        return address;
    }

    public void setaddress(String address) {
        this.address = address;
    }
}
